
public class Matemática {
	
	int somar(int x, int y) { //criação das variáveis x, y do tipo int, para serem usadas com o recem-criado método somar
	
		return x + y; //retorna os valores de x, y já somados 

	}
	
	int somar(int x, int y, int z) { //criação de uma terceira variável (z) do tipo int
		
		return x + y + z;
		
	}
	
	double somar(double x, double y) { //criação das variáveis x, y do tipo double
		
		return x + y;
	}
}
