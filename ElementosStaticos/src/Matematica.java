//nos elementos estáticos (métodos, variáveis, etc), não é necessário instânciar (nem criar) nenhum objeto, já que a classe é chamada diretamente 
//para a execução do método
public class Matematica {
	
	public static int somar(int a, int b) {
		return a+b;
	}
	
	public static int subtrair(int a, int b) {
		return a-b;
	}
	
}
